# 논리데이터 만들기
print(True) # 참
print(False) # 거짓

# 비교 연산자
# 비교연산자는 문자열도 가능
# 반환값은 bool
print(1 == 1) # 같으면 참
print(1 != 1) # 다르면 참
print("abc" == "abc")  
print(2 > 1) # 앞의 항이 더 크면 참
print(2 < 1) # 앞의 항이 더 작으면 참
print(2 >= 2) # 더 크거나 같으면 참
print(2 <= 2) # 더 작거나 같으면 참

# 변수를 사용해서 비교하기
x = 5
y = 10
print(x == y) # 5 == 10
print(x < y) # 5 < 10
